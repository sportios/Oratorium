def get_recommendations(session):
    """Формирует список рекомендаций на основе метрик сессии"""
    recommendations = []
    
    # Темп речи
    if session.speech_rate is not None:
        rate = session.speech_rate
        
        if 130 <= rate <= 150:
            text = "Отличный темп! Вы говорите с идеальной скоростью — вас легко понимать и интересно слушать."
        elif (120 <= rate <= 129) or (151 <= rate <= 160):
            text = "Хороший темп, близкий к идеалу. Чуть-чуть скорректируйте скорость."
        elif (110 <= rate <= 119) or (161 <= rate <= 170):
            text = "Неплохой темп, но есть куда расти. Старайтесь держаться в коридоре 130-150 слов/мин."
        elif (100 <= rate <= 109) or (171 <= rate <= 180):
            text = "Средний результат. Если говорите быстро — замедляйтесь, если медленно — ускоряйтесь."
        elif (90 <= rate <= 99) or (181 <= rate <= 190):
            text = "Темп требует работы. Слушателям может быть некомфортно. Тренируйтесь с метрономом."
        elif (80 <= rate <= 89) or (191 <= rate <= 200):
            text = "Заметные проблемы с темпом. Записывайте себя и следите за скоростью."
        elif (70 <= rate <= 79) or (201 <= rate <= 210):
            text = "Плохой темп. Это серьёзно мешает восприятию речи."
        elif (60 <= rate <= 69) or (211 <= rate <= 220):
            text = "Очень плохой темп. Нужно целенаправленно работать над этим."
        elif (50 <= rate <= 59) or (221 <= rate <= 230):
            text = "Критически медленно или быстро. Начните с простых упражнений на дикцию."
        elif (40 <= rate <= 49) or (231 <= rate <= 240):
            text = "Экстремальный темп. Рекомендуется работа с преподавателем."
        else:
            text = "Недопустимый темп. Полностью пересмотрите манеру речи."
        
        recommendations.append({
            'icon': 'fas fa-tachometer-alt',
            'category': 'Темп речи',
            'text': text
        })
    
    # Громкость
    if session.volume is not None:
        vol = session.volume
        
        if 65 <= vol <= 75:
            text = "Идеальная громкость! Вас отлично слышно, вы не кричите и не шепчете."
        elif (62 <= vol <= 64) or (76 <= vol <= 78):
            text = "Хорошая громкость, близкая к идеалу. Совсем немного скорректируйте уровень."
        elif (59 <= vol <= 61) or (79 <= vol <= 81):
            text = "Неплохая громкость. Старайтесь держаться в диапазоне 65-75 дБ."
        elif (56 <= vol <= 58) or (82 <= vol <= 84):
            text = "Средняя громкость. Обратите внимание на концовки фраз — они часто \"съедаются\"."
        elif (53 <= vol <= 55) or (85 <= vol <= 87):
            text = "Громкость требует внимания. Если тихо — говорите громче, если громко — снизьте тон."
        elif (50 <= vol <= 52) or (88 <= vol <= 90):
            text = "Заметные проблемы с громкостью. Попросите друзей оценивать ваш голос."
        elif (47 <= vol <= 49) or (91 <= vol <= 93):
            text = "Плохая громкость. Вас либо плохо слышно, либо вы утомляете слушателей."
        elif (44 <= vol <= 46) or (94 <= vol <= 96):
            text = "Очень плохая громкость. Срочно работайте над голосом."
        elif (41 <= vol <= 43) or (97 <= vol <= 99):
            text = "Критическая громкость. Практикуйте упражнения на дыхание и голос."
        elif (38 <= vol <= 40) or (100 <= vol <= 102):
            text = "Экстремальная громкость. Нужен контроль каждого слова."
        else:
            text = "Недопустимая громкость. Полностью пересмотрите манеру говорения."
        
        recommendations.append({
            'icon': 'fas fa-volume-up',
            'category': 'Громкость',
            'text': text
        })
    
    # Зрительный контакт
    if session.eye_contact is not None:
        contact = session.eye_contact
        
        if 80 <= contact <= 100:
            text = "Превосходно! Вы отлично держите контакт с аудиторией."
        elif 72 <= contact <= 79:
            text = "Отличный результат. Чуть больше взгляда на слушателей — и будет идеально."
        elif 64 <= contact <= 71:
            text = "Хороший зрительный контакт. Старайтесь не отводить взгляд надолго."
        elif 56 <= contact <= 63:
            text = "Средний результат. Вы смотрите на аудиторию чуть больше половины времени."
        elif 48 <= contact <= 55:
            text = "Зрительного контакта маловато. Поднимайте глаза каждые 5-7 секунд."
        elif 40 <= contact <= 47:
            text = "Половину времени вы говорите \"в никуда\". Аудитория это чувствует."
        elif 32 <= contact <= 39:
            text = "Вы смотрите на слушателей только треть выступления. Пора менять привычку."
        elif 24 <= contact <= 31:
            text = "Критически мало контакта. Выглядит как неуверенность или страх."
        elif 16 <= contact <= 23:
            text = "Вы почти не смотрите на людей. Тренируйтесь смотреть в глаза на камеру."
        elif 8 <= contact <= 15:
            text = "Вы говорите в пол, в потолок, куда угодно, но не на аудиторию."
        else:
            text = "Зрительный контакт практически отсутствует. Это главная проблема."
        
        recommendations.append({
            'icon': 'far fa-eye',
            'category': 'Зрительный контакт',
            'text': text
        })
    
    # Слова-паразиты
    if session.filler_words is not None:
        filler = session.filler_words
        
        if filler <= 2:
            text = "Поздравляю! Практически нет слов-паразитов. Чистая речь — признак мастерства."
        elif filler <= 5:
            text = "Отличный результат. Вы хорошо контролируете речь."
        elif filler <= 8:
            text = "Хорошо. Паразиты есть, но немного. Следите за \"э-э\" и \"ну\"."
        elif filler <= 11:
            text = "Средний результат. Обратите внимание на моменты, когда появляются паразиты."
        elif filler <= 14:
            text = "Есть над чем работать. Попробуйте заменять паразиты на паузы."
        elif filler <= 17:
            text = "Многовато. Записывайте себя и считайте \"э-э\" — осознание поможет."
        elif filler <= 20:
            text = "Плохо. Слова-паразиты отвлекают от смысла. Тренируйтесь дома."
        elif filler <= 23:
            text = "Очень плохо. Половина ваших пауз заполнена паразитами."
        elif filler <= 26:
            text = "Критически много. Без работы над речью не обойтись."
        elif filler <= 29:
            text = "Экстремально много. Каждые несколько слов — паразит."
        else:
            text = "Недопустимо. Вы не контролируете свою речь. Начните с простого: молчание вместо \"э-э\"."
        
        recommendations.append({
            'icon': 'fas fa-comment-slash',
            'category': 'Слова-паразиты',
            'text': text
        })
    
    # Общая рекомендация
    if session.final_score is not None:
        score = session.final_score
        
        if score >= 90:
            text = "Превосходный результат! Вы на уровне профессионала."
        elif score >= 75:
            text = "Отличный результат. Есть небольшие нюансы для доработки."
        elif score >= 60:
            text = "Хороший результат, но есть над чем работать. Сосредоточьтесь на самых слабых метриках."
        elif score >= 40:
            text = "Средний результат. Нужна системная работа над выступлениями."
        elif score >= 20:
            text = "Плохой результат. Рекомендуются регулярные тренировки."
        else:
            text = "Критический результат. Начните с основ: темп и громкость."
        
        recommendations.append({
            'icon': 'fas fa-star',
            'category': 'Общий итог',
            'text': text
        })
    
    return recommendations

