from django.db import models  # Импортируем модели Django для работы с базой данных
from django.utils import timezone  # Чтобы автоматически ставить текущую дату и время

# Определяем модель сессии
class Session(models.Model):
    # Поля модели — это колонки в таблице базы данных
    session_name = models.CharField("Название сессии", max_length=100)  # Название сессии
    datetime = models.DateTimeField("Дата и время", default=timezone.now)  # Дата и время создания
    duration = models.PositiveIntegerField("Длительность", null=True, blank=True)  # Длительность в секундах
    speech_rate = models.PositiveIntegerField("Темп речи (слов/мин)", null=True, blank=True)  # Слов в минуту
    volume = models.PositiveIntegerField("Громкость (дБ)", null=True, blank=True)  # Уровень громкости
    eye_contact = models.PositiveIntegerField("Зрительный контакт (%)", null=True, blank=True)  # Процент зрительного контакта
    filler_words = models.PositiveIntegerField("Слова-паразиты", null=True, blank=True)  # Кол-во слов-паразитов
    final_score = models.FloatField("Итоговый балл", null=True, blank=True)  # Итоговый балл

    # ==========================
    # Метод для вычисления итогового балла
    # ==========================
    def calculate_score(self):
        """
        Вычисляем баллы по справочнику для всех параметров.
        a*35% + b*20% + c*25% + d*20% = итоговый балл
        """
        # --------------------------
        # Темп речи (слов/мин)
        # --------------------------
        a = self.get_score(self.speech_rate, [
            (130, 150, 100), (120, 129, 90), (151, 160, 90),
            (110, 119, 80), (161, 170, 80), (100, 109, 70),
            (171, 180, 70), (90, 99, 60), (181, 190, 60),
            (80, 89, 50), (191, 200, 50), (70, 79, 40),
            (201, 210, 40), (60, 69, 30), (211, 220, 30),
            (50, 59, 20), (221, 230, 20), (40, 49, 10),
            (231, 240, 10)
        ], default=0)

        # --------------------------
        # Громкость (дБ)
        # --------------------------
        b = self.get_score(self.volume, [
            (65, 75, 100), (62, 64, 90), (76, 78, 90),
            (59, 61, 80), (79, 81, 80), (56, 58, 70),
            (82, 84, 70), (53, 55, 60), (85, 87, 60),
            (50, 52, 50), (88, 90, 50), (47, 49, 40),
            (91, 93, 40), (44, 46, 30), (94, 96, 30),
            (41, 43, 20), (97, 99, 20), (38, 40, 10),
            (100, 102, 10)
        ], default=0)

        # --------------------------
        # Зрительный контакт (%)
        # --------------------------
        c = self.get_score(self.eye_contact, [
            (80, 100, 100), (72, 79, 90), (64, 71, 80),
            (56, 63, 70), (48, 55, 60), (40, 47, 50),
            (32, 39, 40), (24, 31, 30), (16, 23, 20),
            (8, 15, 10), (0, 7, 0)
        ], default=0)

        # --------------------------
        # Слова-паразиты
        # --------------------------
        d = self.get_score(self.filler_words, [
            (0, 2, 100), (3, 5, 90), (6, 8, 80),
            (9, 11, 70), (12, 14, 60), (15, 17, 50),
            (18, 20, 40), (21, 23, 30), (24, 26, 20),
            (27, 29, 10), (30, 1000, 0)  # >30 слов → 0 баллов
        ], default=0)

        # --------------------------
        # Считаем итоговый балл с весами
        # a*35% + b*20% + c*25% + d*20%
        # --------------------------
        return a*0.35 + b*0.2 + c*0.25 + d*0.2

    # ==========================
    # Статический метод для определения балла по шкале
    # ==========================
    @staticmethod
    def get_score(value, ranges, default=0):
        """
        value: значение параметра (например, темп речи)
        ranges: список кортежей (мин, макс, балл)
        default: значение по умолчанию, если не попадает ни в один диапазон
        """
        if value is None:  # если значение пустое
            return 0
        # Перебираем все диапазоны
        for r in ranges:
            if r[0] <= value <= r[1]:  # если значение попадает в диапазон
                return r[2]  # возвращаем соответствующий балл
        return default  # если ни один диапазон не подошел

    # ==========================
    # Переопределяем метод save()
    # ==========================
    def save(self, *args, **kwargs):
        """
        Перед сохранением в базу автоматически вычисляем итоговый балл
        """
        self.final_score = self.calculate_score()  # считаем итоговый балл
        super().save(*args, **kwargs)  # вызываем стандартный save Django

    # ==========================
    # Читаемое представление объекта
    # ==========================
    def __str__(self):
        """
        Строковое представление объекта для админки и консоли.
        Показывает: Название сессии (дата и время)
        """
        return f"{self.session_name} ({self.datetime.strftime('%Y-%m-%d %H:%M')})"
