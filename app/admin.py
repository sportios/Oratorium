from django.contrib import admin
from .models import Session  # Импортируем нашу модель из models.py

# Регистрируем модель Session в админке с дополнительными настройками
@admin.register(Session)
class SessionAdmin(admin.ModelAdmin):
    """
    Кастомный класс для отображения модели Session в админке.
    Определяет как данные будут показываться и редактироваться.
    """

    # --------------------------
    # Поля при редактировании записи (форма)
    # --------------------------
    # Эти поля будут отображаться на странице создания/редактирования сессии
    # Исключены: datetime (ставится автоматически) и final_score (вычисляется)
    fields = (
        'session_name',    # Пользователь вводит название
        'duration',        # Вводит длительность
        'speech_rate',     # Вводит темп речи (слов/мин)
        'volume',          # Вводит громкость (дБ)
        'eye_contact',     # Вводит процент зрительного контакта
        'filler_words'     # Вводит количество слов-паразитов
    )
    # Примечание: После сохранения final_score вычислится автоматически

    # --------------------------
    # Поля в списке записей (таблица)
    # --------------------------
    # Эти колонки будут отображаться в общем списке всех сессий
    list_display = (
        'id',              # Уникальный ID сессии (автоматически)
        'session_name',    # Название для быстрой идентификации
        'datetime',        # Когда была создана запись
        'duration',        # Длительность выступления
        'speech_rate',     # Темп речи
        'volume',          # Громкость
        'eye_contact',     # Зрительный контакт
        'filler_words',    # Слова-паразиты
        'final_score'      # Итоговый балл (вычисленный)
    )
    
    